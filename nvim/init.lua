vim.opt.number = true
vim.opt.guicursor = ""

vim.opt.wrap = false
vim.opt.scrolloff = 10
vim.opt.tabstop = 2
vim.opt.softtabstop = 2
vim.opt.shiftwidth = 2
vim.opt.expandtab = true
vim.opt.smartindent = true
vim.opt.cmdheight = 0

